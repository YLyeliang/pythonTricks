def check_multi_frame_v2(md5_list, tag_list):
    """
    multi frame idea:
    统计到达路口对面时的情况，
    status code range: 2 -> 1 -> 3
    1. must across the case 2.
    2.
    """
    crossing_list = []
    start, end = 0, 0
    end_index = []
    while end < len(md5_list):
        if tag_list[end] == 0:
            start += 1
            end += 1
        elif tag_list[end] == 2:
            start += 1
            end += 1
        elif tag_list[end] == 1:
            end += 1
        elif tag_list[end] == 3:
            end_index.append(end + 1)
            # crossing_list += md5_list[max(start, end - 20):end + 1]
            end += 1
            start = end

    # 统计路口对面前20张图像的一个情况，如果是出现 路口中间的情况较多，则表明该图像段为路口图像
    for end in end_index:
        # 对于每个路口对面情景，统计前20张图像的tag数量和最后出现的索引位置
        tag_map = {0: [0, -1], 1: [0, -1], 2: [0, -1], 3: [0, -1]}
        start = max(0, end - 20)
        for i, key in enumerate(tag_list[start:end]):
            num_idx = tag_map[key]
            num_idx[0] += 1
            num_idx[1] = start + i
            tag_map[key] = num_idx

        # 统计到达路口对面时的前20帧情况
        max_ = 0
        max_tag = -1
        for key, val in tag_map.items():
            if val[0] > max_:
                max_tag = key
                max_ = val[0]
        # 之前出现较多的为路口对面或者是路口中间
        if max_tag in [2, 1, 3]:
            if tag_map[2][1] >= 0:  # 这一段内有路口起点
                crossing_list += md5_list[tag_map[2][1]:end]
            else:
                crossing_list += md5_list[start:end]

    for start in end_index:
        tag_map = {0: [0, -1], 1: [0, -1], 2: [0, -1], 3: [0, -1]}
        end = min(start + 20, len(tag_list))
        for i, key in enumerate(tag_list[start:end]):
            num_idx = tag_map[key]
            num_idx[0] += 1
            num_idx[1] = start + i
            tag_map[key] = num_idx

        # 统计到达路口起点时后20帧的情况
        max_ = 0
        max_tag = -1
        for key, val in tag_map.items():
            if val[0] > max_:
                max_tag = key
                max_ = val[0]
        # 之后出现较多的为路口中间或者路口起点
        if max_tag in [2, 1]:
            if tag_map[3][1] >= 0:  # 这一段内有路口对面
                crossing_list += md5_list[start:tag_map[3][0]]
            else:
                crossing_list += md5_list[start:end]


    ret_md5 = list(set(crossing_list))
    return ret_md5




